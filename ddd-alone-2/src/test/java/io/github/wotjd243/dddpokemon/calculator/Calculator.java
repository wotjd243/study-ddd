package io.github.wotjd243.dddpokemon.calculator;

public class Calculator {
    int add(final int i, final int j) {
        return i + j;
    }

    int subtract(final int i, final int j) {
        return i - j;
    }

    int multiply(final int i, final int j) {
        return i * j;
    }

    int divide(final int i, final int j) {
        return i / j;
    }
}
